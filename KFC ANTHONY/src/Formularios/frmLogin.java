    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        try{
            frmCarta menu=new frmCarta();
            Conectar cliente = new Conectar();
            if (cliente.ValidarUsuario(txtUsuario.getText(), txtContra.getText())==1)
            {
                Conectar conectar =new Conectar();
                menu.setVisible(true);
                this.setVisible(false);
                conectar.OptenerIdUsu(txtUsuario.getText(), txtContra.getText());
            }
            else if(txtUsuario.getText().equals("")||txtContra.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Uno o mas campos estan vacios, por favr ingrese los datos.");
            }
            else
            {
                contador++;
                JOptionPane.showMessageDialog(this,"Datos incorrectos, le quedan: "+ (3-contador));

                if(contador==3)
                {
                    JOptionPane.showMessageDialog(this,"Exedio el numero de intentos "+ (3-contador));
                    System.exit(0);
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Ingrese los datos requeridos");
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

